<br />
<span style="color: red;">После получения имён DNS-серверов нужно зайти на сайт  <a href="http://www.nic.ru" target="_blank"><span style="color: red;"> сайт РуЦентра </span></a>.</span> Раздел &quot;Для клиентов&quot;, после ввода данных договора необходимо в разделе &quot;Услуги&quot; выбрать пункт &quot;Просмотр и изменение данных&quot;. Далее в графе &quot;Изменить параметры&quot; нажать на ссылку &quot;DNS-сервера домена&quot; и внести в появившуюся форму имена полученных в письме DNS-серверов. 
