<br />
 (<span style="color: red;">Тех. Поддержка</span>) 
<br />
 
<br />
 Отправить заявку на support@osg.ru примерно такого содержания: 
<br />
 
<br />
 Прошу Вас принять на обслуживание доменное имя adress2.ru и заменить/добавить им/к ныне действующему имени моего сайта adress.ru. 
