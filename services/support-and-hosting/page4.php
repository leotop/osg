 Компьютер (сервер) для установки программного комплекса &quot;OSG Server System&quot; должен удовлетворять следующим требованиям: 
<br />
 
<br />
 Рекомендуемая минимальная конфигурация &ndash; Pentium III, тактовая частота 1000 Мгц, оперативная память 512 Мб. Используемые операционные системы &ndash;Microsoft Windows Server 2000/2003. 
<br />
 
<br />
 Перед установкой программы на сервер необходимо: 
<br />
 <blockquote> 
  <li> подключить компьютер к сети Интернет; </li>
 
  <br />
 
  <li>установить SMTP-сервер (возможно использование внешнего анонимного SMTP-сервера); </li>
 
  <br />
 
  <li>установить Microsoft SQL Server 2000-2005 (возможно использование внешнего Microsoft SQL Server 2000-2005); </li>
 
  <br />
 
  <li> установить Microsoft Internet Information Services версии 5.0 (и выше); </li>
 
  <br />
 
  <li> установить Microsoft .Net Framework 1.1; (включен в поставку) </li>
 
  <br />
 
