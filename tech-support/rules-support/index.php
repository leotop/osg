<p><strong>Приоритет 2</strong>. Аварийная ситуация при которой ПО функционирует частично, либо с низкой производительностью;</p>
 
<p><strong>Приоритет 3</strong>. ПО функционирует, требуется Техническая консультация.</p>
 
